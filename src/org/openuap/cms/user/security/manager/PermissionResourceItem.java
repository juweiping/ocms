package org.openuap.cms.user.security.manager;

/**
 * <p>
 * 资源权限控制项,如对论坛帖子的移动、删除、编辑、新增操作
 * </p>
 * 
 * <p>
 * $Id: PermissionResourceItem.java 3939 2010-10-27 08:41:36Z orangeforjava $
 * </p>
 * 
 * @author Joseph
 * @version 1.0
 */
public class PermissionResourceItem implements java.io.Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 4960555061334786293L;
	/** 资源控制项键值，原来是长整型的移位数，从0开始. */
	private String key;
	/** 资源控制项名(英文). */
	private String name;
	/** 资源控制项显示名(中文). */
	private String title;

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((key == null) ? 0 : key.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		final PermissionResourceItem other = (PermissionResourceItem) obj;
		if (key == null) {
			if (other.key != null)
				return false;
		} else if (!key.equals(other.key))
			return false;
		return true;
	}

}
